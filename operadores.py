# Operadores Booleanos
"""
    - Maior que: >
    - Menor que: <
    - Maior ou igual a: >=
    - Menor ou igual a: <=
    - Comparação: ==
    - Diferente ou igual: !=
"""


idade1 = 10
idade2 = 15
preço1 = 2.77
preço2 = 2.65
preço3 = preço2

print(idade1 > idade2)
print(idade1 <= idade2)
print("Python" == "python")
print("banana" != "abacaxi")
print(preço1 >= preço2)
print(preço2 >= preço3)